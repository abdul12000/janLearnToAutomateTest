package javaBasics;

public class FFWhileLoop {
    public static void main(String[] args) {

        int a = 5;
        while (a < 10) {
            System.out.println(a);
            a++;
        }

        int j =5;
//do while
        do{
            System.out.println(j);
            j++;
        }while (j <10);

    }
}
